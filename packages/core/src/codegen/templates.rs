//! Soroban policy code templates.
//!
//! All templates are static strings. The emitter does variable substitution
//! via simple token replacement — no proc-macro or external templating engine.

/// Tokens used in template substitution.
pub struct TemplateVars {
    pub crate_name: String,
    pub policy_struct_name: String,
    pub policy_display_name: String,
    pub spending_limit_cap: String,
    pub spending_limit_asset: String,
    pub spending_limit_window: String,
    pub time_window_secs: String,
    pub slippage_percent: String,
    pub context_contracts: String,
    pub context_functions: String,
    pub context_lifetime_secs: String,
    pub policy_description: String,
    pub rationale: String,
}

impl Default for TemplateVars {
    fn default() -> Self {
        TemplateVars {
            crate_name: "custom-policy".to_string(),
            policy_struct_name: "CustomPolicy".to_string(),
            policy_display_name: "Custom Policy".to_string(),
            spending_limit_cap: "100_000_000".to_string(),
            spending_limit_asset: "CUSDC".to_string(),
            spending_limit_window: "86400".to_string(),
            time_window_secs: "86400".to_string(),
            slippage_percent: "5".to_string(),
            context_contracts: "[]".to_string(),
            context_functions: "[]".to_string(),
            context_lifetime_secs: "31536000".to_string(),
            policy_description: "Generated by OZ Policy Builder".to_string(),
            rationale: "".to_string(),
        }
    }
}

/// Replace all `{{TOKEN}}` markers in a template string.
pub fn render(template: &str, vars: &TemplateVars) -> String {
    template
        .replace("{{CRATE_NAME}}", &vars.crate_name)
        .replace("{{POLICY_STRUCT}}", &vars.policy_struct_name)
        .replace("{{POLICY_DISPLAY}}", &vars.policy_display_name)
        .replace("{{SPENDING_CAP}}", &vars.spending_limit_cap)
        .replace("{{SPENDING_ASSET}}", &vars.spending_limit_asset)
        .replace("{{SPENDING_WINDOW}}", &vars.spending_limit_window)
        .replace("{{TIME_WINDOW_SECS}}", &vars.time_window_secs)
        .replace("{{SLIPPAGE_PERCENT}}", &vars.slippage_percent)
        .replace("{{CONTEXT_CONTRACTS}}", &vars.context_contracts)
        .replace("{{CONTEXT_FUNCTIONS}}", &vars.context_functions)
        .replace("{{CONTEXT_LIFETIME}}", &vars.context_lifetime_secs)
        .replace("{{POLICY_DESC}}", &vars.policy_description)
        .replace("{{RATIONALE}}", &vars.rationale)
}

