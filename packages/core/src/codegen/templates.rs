pub const LIB_RS_GENERATE: &str = r#"//! {{POLICY_DISPLAY}} — generated by OZ Policy Builder
//!
//! Generation mode: implements the OZ Policy trait directly.
//!
//! ⚠  REVIEW BEFORE DEPLOYING. Never deploy automatically.
//!
//! Rationale: {{RATIONALE}}

#![no_std]
use soroban_sdk::{
    contract, contracterror, contractimpl, contracttype,
    Address, Env, Symbol, Vec,
};

// ── Storage keys ─────────────────────────────────────────────────────────────
// Storage is always scoped by (account_address, context_rule_id) to prevent
// cross-account / cross-context data leaks. See OZ accounts security model.

#[contracttype]
#[derive(Clone)]
pub enum DataKey {
    /// Cumulative spending within current window: (account, ctx_id) → SpendState
    SpendState(Address, Symbol),
    /// Window start ledger: (account, ctx_id) → u32
    WindowStart(Address, Symbol),
}

#[contracttype]
#[derive(Clone)]
pub struct SpendState {
    pub spent: i128,
    pub window_start_ledger: u32,
}

// ── Error codes ───────────────────────────────────────────────────────────────
#[contracterror]
#[derive(Clone, Debug, Eq, PartialEq)]
#[repr(u32)]
pub enum PolicyError {
    SpendingLimitExceeded = 1,
    OutsideTimeWindow     = 2,
    UnauthorizedContract  = 3,
    UnauthorizedFunction  = 4,
    SlippageExceeded      = 5,
    NotInstalled          = 6,
}

// ── Constants ─────────────────────────────────────────────────────────────────
/// Max outbound amount per window (raw, 7 decimals).
const SPENDING_CAP: i128 = {{SPENDING_CAP}};
/// Time window in ledgers (1 ledger ≈ 5 s; window in seconds / 5).
const WINDOW_LEDGERS: u32 = {{SPENDING_WINDOW}} / 5;
/// Max slippage in basis points (100 = 1%).
const MAX_SLIPPAGE_BPS: u32 = {{SLIPPAGE_PERCENT}} * 100;

// ── Contract ──────────────────────────────────────────────────────────────────
#[contract]
pub struct {{POLICY_STRUCT}};

#[contractimpl]
impl {{POLICY_STRUCT}} {
    /// Called once when the policy is installed on a context rule.
    pub fn install(
        env: Env,
        account: Address,
        context_rule_id: Symbol,
    ) {
        account.require_auth();
        let key = DataKey::SpendState(account.clone(), context_rule_id.clone());
        env.storage().persistent().set(&key, &SpendState {
            spent: 0,
            window_start_ledger: env.ledger().sequence(),
        });
        let wk = DataKey::WindowStart(account, context_rule_id);
        env.storage().persistent().set(&wk, &env.ledger().sequence());
    }

    /// Called before each invocation to decide permit/deny.
    pub fn can_enforce(
        env: Env,
        account: Address,
        context_rule_id: Symbol,
        _contract: Address,
        function: Symbol,
    ) -> Result<(), PolicyError> {
        let key = DataKey::SpendState(account.clone(), context_rule_id.clone());
        let mut state: SpendState = env
            .storage()
            .persistent()
            .get(&key)
            .ok_or(PolicyError::NotInstalled)?;

        // Reset window if expired
        let current_ledger = env.ledger().sequence();
        if current_ledger.saturating_sub(state.window_start_ledger) >= WINDOW_LEDGERS {
            state.spent = 0;
            state.window_start_ledger = current_ledger;
        }

        Ok(())
    }

    /// Called during invocation to enforce the policy.
    pub fn enforce(
        env: Env,
        account: Address,
        context_rule_id: Symbol,
        _contract: Address,
        _function: Symbol,
        amount: i128,
    ) -> Result<(), PolicyError> {
        let key = DataKey::SpendState(account.clone(), context_rule_id.clone());
        let mut state: SpendState = env
            .storage()
            .persistent()
            .get(&key)
            .ok_or(PolicyError::NotInstalled)?;

        let current_ledger = env.ledger().sequence();
        if current_ledger.saturating_sub(state.window_start_ledger) >= WINDOW_LEDGERS {
            state.spent = 0;
            state.window_start_ledger = current_ledger;
        }

        state.spent = state.spent.saturating_add(amount);
        if state.spent > SPENDING_CAP {
            return Err(PolicyError::SpendingLimitExceeded);
        }

        env.storage().persistent().set(&key, &state);
        Ok(())
    }

    /// Called when the context rule is removed — clean up storage.
    pub fn uninstall(
        env: Env,
        account: Address,
        context_rule_id: Symbol,
    ) {
        let key = DataKey::SpendState(account.clone(), context_rule_id.clone());
        env.storage().persistent().remove(&key);
        let wk = DataKey::WindowStart(account, context_rule_id);
        env.storage().persistent().remove(&wk);
    }
}
"#;

