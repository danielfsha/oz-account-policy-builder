#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_render_replaces_tokens() {
        let vars = TemplateVars {
            crate_name: "my-policy".to_string(),
            policy_display_name: "My Policy".to_string(),
            ..Default::default()
        };
        let out = render("name = \"{{CRATE_NAME}}\" // {{POLICY_DISPLAY}}", &vars);
        assert!(out.contains("my-policy"));
        assert!(out.contains("My Policy"));
    }

    #[test]
    fn test_cargo_toml_renders() {
        let vars = TemplateVars {
            crate_name: "blend-yield-claim".to_string(),
            policy_description: "Blend yield claim policy".to_string(),
            ..Default::default()
        };
        let out = render(CARGO_TOML, &vars);
        assert!(out.contains("blend-yield-claim"));
        assert!(out.contains("Blend yield claim policy"));
    }
}
